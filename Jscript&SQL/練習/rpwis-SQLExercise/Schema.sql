COMMENT ON COLUMN EMPLOYEES.ADDRESS IS '地址';
COMMENT ON COLUMN EMPLOYEES.MANAGERID IS '主管ID';
COMMENT ON COLUMN EMPLOYEES.DEPARTMENTID IS '部門ID';

COMMENT ON COLUMN TRAINING.EMPLOYEEID IS '員工編號';
COMMENT ON COLUMN TRAINING.TYPE IS '訓練類型';
